 
public class Treasure {

    private String name;
    private int x;
    private int y;

    public Treasure(String name, int x, int y) {
        this.name = name;
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

}
